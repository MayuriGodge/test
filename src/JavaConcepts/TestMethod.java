package JavaConcepts;

public class TestMethod extends MethodOverriding
{
	public static void main(String[] args)
	{
		m1();
	}
	public static void m1()
	{
		System.out.println("I am in m1 function of Test Class");
	}
	public void m3()
	{
		System.out.println("I am in m3 function of Test Class");
	}
}
